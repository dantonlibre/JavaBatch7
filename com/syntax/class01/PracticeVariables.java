package com.syntax.class01;

   /*HW
    * Create variables for all 8 primitive Data Types
    * (byte, short, int, float, double, char, boolean).
    * I want you to print out each variable on a a separate line 
    * and test its number range(capacity)
    */

public class PracticeVariables {

	public static void main(String[] args) {
		
		byte num = 77;
		short num1  = 5430;
		int num2 = 234567;
		long num3 = 494950540393L;
		float num4 = 32.484859595959F;
		double num5 = 72.8686969709707;
		char math = '-';
		boolean surprise = true;
		
		
		System.out.println(num);
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		System.out.println(math);
		System.out.println(surprise);
		
	}
}
