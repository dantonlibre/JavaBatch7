package com.syntax.class01;

public class PrimitiveDataTypes {
      
	public static void main(String[] args) {
	
    /**Primitive Data Types
	 * 	   
	 * byte
	 * short
	 * int
	 * long
	 * float
	 * double
	 * char
	 * boolean
	 */
    	   
    	   //Whole Number Data Types
		
		
		byte num = 10;
		
		short num1 = 100;
		
		int num2 = 1000;
		
		//l or L
		long num3 = 12987665444L;
		
		//Decimal Number Data Types
		//f or F
		float num4 = 12.9F;
		double num5 = 0.888;
		
		//Non-Number Data Types
		
		char add = '+';
		boolean run = true;
		boolean run1 = false;
		
		
			
		
	
       }	
}
