package com.syntax.class12;

public class ArrayRecap {
	public static void main(String[] args) {
		
		String[] colors=new String[3];
		colors[0]="red";
		colors[1]="yelow";
		colors[2]="blue";
		System.out.println(colors.length);
		String color =null;
		for(int i=0;i<colors.length;i++) {
		color=colors [i];
	}
		System.out.println(color);
		
		
	}
}

